using QuestPDF.Infrastructure;

namespace Dodostays.Api.Modules.Payments.Invoices;

public sealed class QuestPdfInvoiceGenerator : IInvoiceGenerator
{
    private readonly IInvoiceSequenceService _sequenceService;
    private readonly IInvoicePdfStorage _storage;
    private readonly PaymentOptions _paymentOptions;
    private readonly ILogger<QuestPdfInvoiceGenerator> _logger;

    private const decimal VatRate = 0.15m; // Mauritius standard VAT rate

    public QuestPdfInvoiceGenerator(
        IInvoiceSequenceService sequenceService,
        IInvoicePdfStorage storage,
        IOptions<PaymentOptions> paymentOptions,
        ILogger<QuestPdfInvoiceGenerator> logger)
    {
        _sequenceService = sequenceService;
        _storage = storage;
        _paymentOptions = paymentOptions.Value;
        _logger = logger;
    }

    public async Task<Invoice> GenerateGuestStayAsync(GuestInvoiceInput input, CancellationToken ct)
    {
        var invoiceNumber = await _sequenceService.NextNumberAsync(InvoiceKind.GuestStay, ct);
        var issueDate = DateTimeOffset.UtcNow;

        // Calculate totals
        var nightsTotal = input.Nights * input.NightlyRateNetMur;
        var cleaningTotal = input.CleaningFeeNetMur;
        var netTotal = nightsTotal + cleaningTotal;
        var vat = netTotal * VatRate;
        var grossTotal = netTotal + vat;

        // Generate PDF
        var pdfBytes = Document.Create(container =>
        {
            container.Page(page =>
            {
                page.Size(PageSizes.A4);
                page.Margin(40);
                page.DefaultTextStyle(x => x.FontSize(10));

                page.Header().Element(c => ComposeGuestInvoiceHeader(c, invoiceNumber, issueDate));
                page.Content().Element(c => ComposeGuestInvoiceContent(
                    c,
                    input,
                    nightsTotal,
                    cleaningTotal,
                    netTotal,
                    vat,
                    grossTotal));
                page.Footer().AlignCenter().Text("Generated by DodoStays. Contact support@dodostays.com for queries.")
                    .FontSize(8).FontColor(Colors.Grey.Medium);
            });
        }).GeneratePdf();

        // Store PDF
        var storagePath = await _storage.StoreAsync(invoiceNumber, pdfBytes, ct);

        _logger.LogInformation("Generated guest stay invoice {InvoiceNumber} for booking {BookingId}",
            invoiceNumber, input.BookingId);

        return new Invoice
        {
            Id = Guid.NewGuid(),
            Number = invoiceNumber,
            Kind = InvoiceKind.GuestStay,
            BookingId = input.BookingId,
            PayoutId = null,
            IssuedToDisplayName = input.GuestDisplayName,
            IssuedToVatNumber = input.GuestVatNumber,
            GrossMur = grossTotal,
            VatMur = vat,
            NetMur = netTotal,
            PdfStoragePath = storagePath,
            IssuedAt = issueDate
        };
    }

