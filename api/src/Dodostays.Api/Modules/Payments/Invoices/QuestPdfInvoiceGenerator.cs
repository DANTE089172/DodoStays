    private void ComposeGuestInvoiceHeader(IContainer container, string invoiceNumber, DateTimeOffset issueDate)
    {
        container.Column(column =>
        {
            column.Spacing(10);

            // Title row with INVOICE and metadata
            column.Item().Row(row =>
            {
                row.RelativeItem().AlignLeft().Text("INVOICE")
                    .FontSize(24)
                    .Bold()
                    .LetterSpacing(1.5f)
                    .FontColor("#FF9F6A");

                row.RelativeItem().AlignRight().Column(col =>
                {
                    col.Item().Text($"Invoice #: {invoiceNumber}").FontSize(9);
                    col.Item().Text($"Issue Date: {issueDate:yyyy-MM-dd}").FontSize(9);
                    col.Item().Text($"Due Date: {issueDate:yyyy-MM-dd}").FontSize(9);
                });
            });

            column.Item().LineHorizontal(1).LineColor(Colors.Grey.Lighten2);
        });
    }

