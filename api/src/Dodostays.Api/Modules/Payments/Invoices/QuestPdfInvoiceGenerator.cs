    private void ComposeGuestInvoiceContent(
        IContainer container,
        GuestInvoiceInput input,
        decimal nightsTotal,
        decimal cleaningTotal,
        decimal netTotal,
        decimal vat,
        decimal grossTotal)
    {
        container.PaddingVertical(20).Column(column =>
        {
            column.Spacing(15);

            // Merchant and Bill-To blocks
            column.Item().Row(row =>
            {
                row.RelativeItem().Column(col =>
                {
                    col.Item().Text("From:").FontSize(9).Bold().FontColor(Colors.Grey.Medium);
                    col.Item().Text(_paymentOptions.MerchantOfRecord).FontSize(10).Bold();
                    col.Item().Text(_paymentOptions.MerchantAddress).FontSize(9);
                    if (!string.IsNullOrWhiteSpace(_paymentOptions.MerchantVatNumber))
                        col.Item().Text($"VAT: {_paymentOptions.MerchantVatNumber}").FontSize(9);
                });

                row.RelativeItem().Column(col =>
                {
                    col.Item().AlignRight().Text("Bill To:").FontSize(9).Bold().FontColor(Colors.Grey.Medium);
                    col.Item().AlignRight().Text(input.GuestDisplayName).FontSize(10).Bold();
                    if (!string.IsNullOrWhiteSpace(input.GuestVatNumber))
                        col.Item().AlignRight().Text($"VAT: {input.GuestVatNumber}").FontSize(9);
                });
            });

            // Trip details
            column.Item().PaddingTop(10).Column(col =>
            {
                col.Item().Text("Trip Details:").FontSize(9).Bold().FontColor(Colors.Grey.Medium);
                col.Item().Text($"{input.ListingTitle} — {input.CheckIn:yyyy-MM-dd} to {input.CheckOut:yyyy-MM-dd} ({input.Nights} nights)")
                    .FontSize(10);
            });

            // Line items table
            column.Item().PaddingTop(10).Table(table =>
            {
                table.ColumnsDefinition(columns =>
                {
                    columns.RelativeColumn(3); // Description
                    columns.ConstantColumn(60); // Qty
                    columns.ConstantColumn(80); // Unit Net
                    columns.ConstantColumn(80); // Net Total
                });

                // Header
                table.Header(header =>
                {
                    header.Cell().Element(CellStyle).Text("Description").Bold();
                    header.Cell().Element(CellStyle).AlignRight().Text("Qty").Bold();
                    header.Cell().Element(CellStyle).AlignRight().Text("Unit Net").Bold();
                    header.Cell().Element(CellStyle).AlignRight().Text("Net Total").Bold();

                    static IContainer CellStyle(IContainer c) => c.BorderBottom(1).BorderColor(Colors.Grey.Lighten2).PaddingBottom(5);
                });

                // Accommodation line
                table.Cell().Element(CellStyle).Text($"{input.ListingTitle} — {input.Nights} nights @ MUR {input.NightlyRateNetMur:N2} net");
                table.Cell().Element(CellStyle).AlignRight().Text(input.Nights.ToString());
                table.Cell().Element(CellStyle).AlignRight().Text($"MUR {input.NightlyRateNetMur:N2}");
                table.Cell().Element(CellStyle).AlignRight().Text($"MUR {nightsTotal:N2}");

                // Cleaning fee line (if any)
                if (input.CleaningFeeNetMur > 0)
                {
                    table.Cell().Element(CellStyle).Text("Cleaning fee");
                    table.Cell().Element(CellStyle).AlignRight().Text("1");
                    table.Cell().Element(CellStyle).AlignRight().Text($"MUR {input.CleaningFeeNetMur:N2}");
                    table.Cell().Element(CellStyle).AlignRight().Text($"MUR {cleaningTotal:N2}");
                }

                static IContainer CellStyle(IContainer c) => c.PaddingVertical(5);
            });

            // Totals section
            column.Item().PaddingTop(15).AlignRight().Column(col =>
            {
                col.Item().Row(row =>
                {
                    row.ConstantItem(120).Text("Sub-total (net):").FontSize(9);
                    row.ConstantItem(100).AlignRight().Text($"MUR {netTotal:N2}").FontSize(9);
                });
                col.Item().Row(row =>
                {
                    row.ConstantItem(120).Text("VAT 15%:").FontSize(9);
                    row.ConstantItem(100).AlignRight().Text($"MUR {vat:N2}").FontSize(9);
                });
                col.Item().PaddingTop(5).BorderTop(1).BorderColor(Colors.Grey.Lighten2);
                col.Item().PaddingTop(5).Row(row =>
                {
                    row.ConstantItem(120).Text("Total:").FontSize(11).Bold();
                    row.ConstantItem(100).AlignRight().Text($"MUR {grossTotal:N2}").FontSize(11).Bold();
                });
            });

            // Damage deposit (if any) — shown separately, not included in VAT/total
            if (input.DamageDepositMur > 0)
            {
                column.Item().PaddingTop(20).AlignRight().Column(col =>
                {
                    col.Item().BorderTop(1).BorderColor(Colors.Grey.Lighten3).PaddingTop(10);
                    col.Item().Row(row =>
                    {
                        row.ConstantItem(220).Text("Damage deposit (held — refundable):").FontSize(9).Italic();
                        row.ConstantItem(100).AlignRight().Text($"MUR {input.DamageDepositMur:N2}").FontSize(9).Italic();
                    });
                });
            }
        });
    }

